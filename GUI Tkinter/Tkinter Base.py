from tkinter import *
# GUI Tkinter is the standard GUI library for Python. Here, we’ll start to develop our first GUI Tkinter GUI.
# GUI Tkinter provides a powerful object-oriented interface to the Tk GUI toolkit.

# To create the main window, GUI Tkinter offers a method, ‘Tk’.
root = Tk()  # root is an instance of Tk class, creates the base for GUI

# There is a method known by the name mainloop(), which is used when your application is ready to run.
# This is an infinite loop used to run the application, wait for an event to occur, and process the event as long as
# the window is not closed.
root.mainloop()  # mainloop writes the code logic in GUI
